package challenges.day_037;

public class Day037 {
    public static void main(String[] args) {
        int i = 5;

        // Operador de prefixo
        System.out.println(++i);  // Imprime: 6
        System.out.println(i);    // Imprime: 6

        i = 5;

        // Operador de sufixo
        System.out.println(i++);  // Imprime: 5
        System.out.println(i);    // Imprime: 6
    }
}
